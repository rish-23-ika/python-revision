#Create a function that takes two numbers as parameters and returns their sum.
def sum(a,b):
    return(a+b)

a = int(input("Enter first number: "))
b = int(input("Enter second number: "))
result = sum(a,b)
print(f"the sum of {a} and {b} is: {result}")

